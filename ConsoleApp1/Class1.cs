public static class Calculadora
{


    public static int Somar(int Valor1, int Valor2) => Valor1 + Valor2;

    public static int Subtrair(int Valor1, int Valor2) => Valor1 - Valor2;

    public static int Dividir(int Valor1, int Valor2) => Valor1 / Valor2;

    public static int Multiplicar(int Valor1, int Valor2) => Valor1 * Valor2;




}